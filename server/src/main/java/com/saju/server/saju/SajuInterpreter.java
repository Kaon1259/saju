    public static String interpretInteractions(List<SajuInteraction> interactions) {
        if (interactions == null || interactions.isEmpty()) return "사주 내 특별한 합충형해 관계가 없습니다.";
        StringBuilder sb = new StringBuilder();
        for (SajuInteraction inter : interactions) {
            sb.append("▸ ").append(inter.getDescription()).append(" (").append(inter.getPillar1());
            if (inter.getPillar3() != null) sb.append("-").append(inter.getPillar2()).append("-").append(inter.getPillar3());
            else sb.append("-").append(inter.getPillar2());
            sb.append(")\n");
            // Add meaning based on type
            switch (inter.getType()) {
                case "합":
                    sb.append("  → 조화와 결합의 기운. ");
                    if (inter.getResultElement() != null) sb.append(inter.getResultElement()).append(" 오행의 기운이 강화됩니다. ");
                    if ("육합".equals(inter.getSubType())) sb.append("두 기둥 간 친밀한 관계로, 대인관계와 협력에 유리합니다.");
                    else if ("삼합".equals(inter.getSubType())) sb.append("세 기둥이 하나로 뭉치는 강력한 합으로, 큰 성취를 이룰 수 있습니다.");
                    else if ("방합".equals(inter.getSubType())) sb.append("같은 방향의 기운이 모여 안정적이고 든든한 에너지를 형성합니다.");
                    break;
                case "충":
                    sb.append("  → 충돌과 변화의 기운. 갈등이 있으나 변화를 통해 성장할 수 있습니다. 직업, 거주지 등의 변동수가 있을 수 있습니다.");
                    break;
                case "형":
                    sb.append("  → 시련과 단련의 기운. ");
                    if ("삼형".equals(inter.getSubType())) sb.append("세 기둥이 서로 부딪혀 큰 시련이 있으나 극복하면 강해집니다.");
                    else if ("무례형".equals(inter.getSubType())) sb.append("예의와 질서의 문제가 생길 수 있으니 언행에 주의하세요.");
                    else if ("자형".equals(inter.getSubType())) sb.append("자기 자신과의 갈등. 내면의 성찰이 필요합니다.");
                    break;
                case "해":
                    sb.append("  → 은근한 방해의 기운. 겉으로 드러나지 않는 갈등이나 배신에 주의하세요. 건강 관리도 필요합니다.");
                    break;
            }
            sb.append("\n\n");
        }
        return sb.toString().trim();
    }

