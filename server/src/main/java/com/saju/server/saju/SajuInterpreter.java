    /**
     * 오행 균형 분석 - based on the distribution of five elements
     */
    public static String interpretFiveElements(Map<String, Integer> elements,
                                               String strongest, String weakest,
                                               int yangCount, int yinCount) {
        StringBuilder sb = new StringBuilder();

        // 1. 오행 분포 현황
        sb.append("【오행 분포】 ");
        for (Map.Entry<String, Integer> entry : elements.entrySet()) {
            sb.append(entry.getKey()).append(" ").append(entry.getValue()).append("개, ");
        }
        sb.setLength(sb.length() - 2);
        sb.append("\n\n");

        // 2. 가장 강한 오행 해석
        sb.append("【강한 기운: ").append(strongest).append("】 ");
        sb.append(interpretStrongestElement(strongest));
        sb.append("\n\n");

        // 3. 가장 약한 오행 해석 및 보완 방법
        sb.append("【부족한 기운: ").append(weakest).append("】 ");
        sb.append(interpretWeakestElement(weakest));
        sb.append("\n\n");

        // 4. 음양 균형
        sb.append("【음양 균형】 ");
        sb.append("양(陽) ").append(yangCount).append("개, 음(陰) ").append(yinCount).append("개. ");
        if (yangCount > yinCount + 2) {
            sb.append("양의 기운이 강하여 적극적이고 활동적인 성향이 두드러집니다. " +
                    "외향적이며 추진력이 강하지만, 때로는 성급하거나 지나치게 앞서 나갈 수 있습니다. " +
                    "차분하게 내면을 살피는 시간을 갖고, 수용과 경청의 자세를 가다듬으면 더욱 조화로운 삶이 될 것입니다.");
        } else if (yinCount > yangCount + 2) {
            sb.append("음의 기운이 강하여 사려 깊고 내면이 풍부한 성향이 두드러집니다. " +
                    "신중하고 관찰력이 뛰어나지만, 때로는 우유부단하거나 소극적일 수 있습니다. " +
                    "적극적으로 자신을 표현하고 행동으로 옮기는 연습을 하면 더 큰 성과를 이룰 수 있습니다.");
        } else {
            sb.append("음양의 기운이 비교적 균형을 이루고 있어 안정적인 성품을 지니고 있습니다. " +
                    "상황에 따라 적극적이기도 하고 신중하기도 한 유연한 태도를 보이며, " +
                    "주변과 조화롭게 어울리는 능력이 뛰어납니다. 이 균형을 잘 유지해 나가시면 좋겠습니다.");
        }

        return sb.toString();
    }

