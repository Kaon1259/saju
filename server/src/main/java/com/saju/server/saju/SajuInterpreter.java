    /**
     * 오늘의 운세 생성 - category-specific fortunes based on saju
     */
    public static SajuResult.CategoryFortune generateTodayFortune(SajuResult result, LocalDate today) {
        // Calculate today's day pillar
        SajuPillar todayPillar = SajuCalculator.calculateDayPillar(today);

        // Get day master index
        int dayMasterIndex = getDayMasterIndex(result.getDayMaster());
        if (dayMasterIndex < 0) dayMasterIndex = 0;

        // Calculate 십성 of today's stem to day master
        int todaySipsung = SajuCalculator.calculateSipsung(dayMasterIndex, todayPillar.getStemIndex());

        // Today's element
        int todayElement = todayPillar.getStemElement();
        int dayMasterElement = SajuConstants.CHEONGAN_OHENG[dayMasterIndex];

        // Determine relationship
        boolean isProducingMe = SajuConstants.OHENG_PRODUCES[todayElement] == dayMasterElement;
        boolean isOvercomingMe = SajuConstants.OHENG_OVERCOMES[todayElement] == dayMasterElement;
        boolean iProduceIt = SajuConstants.OHENG_PRODUCES[dayMasterElement] == todayElement;
        boolean iOvercomeIt = SajuConstants.OHENG_OVERCOMES[dayMasterElement] == todayElement;
        boolean sameElement = todayElement == dayMasterElement;

        // Use a deterministic seed for consistency
        long seed = (long) result.getDayMaster().hashCode() + today.hashCode();
        Random random = new Random(seed);

        // Generate score based on relationship
        int baseScore;
        if (isProducingMe) baseScore = 85;
        else if (sameElement) baseScore = 75;
        else if (iProduceIt) baseScore = 65;
        else if (iOvercomeIt) baseScore = 70;
        else baseScore = 55;
        int score = Math.min(100, Math.max(30, baseScore + random.nextInt(21) - 10));

        // Generate each category fortune
        String overall = generateOverallFortune(todaySipsung, score, random);
        String love = generateLoveFortune(todaySipsung, random);
        String money = generateMoneyFortune(todaySipsung, random);
        String health = generateHealthFortune(todayElement, dayMasterElement, random);
        String work = generateWorkFortune(todaySipsung, random);

        int luckyNumber = random.nextInt(99) + 1;
        String[] colors = {"빨강", "파랑", "노랑", "초록", "보라", "흰색", "검정", "분홍", "주황", "금색", "은색", "하늘색"};
        String luckyColor = colors[random.nextInt(colors.length)];

        return SajuResult.CategoryFortune.builder()
                .overall(overall)
                .love(love)
                .money(money)
                .health(health)
                .work(work)
                .score(score)
                .luckyNumber(luckyNumber)
                .luckyColor(luckyColor)
                .build();
    }

