    /**
     * Add all interpretations to a SajuResult
     */
    public static void interpret(SajuResult result, LocalDate today) {
        result.setPersonalityReading(interpretDayMaster(result.getDayMaster()));
        result.setElementAnalysis(interpretFiveElements(
                result.getFiveElements(),
                result.getStrongestElement(),
                result.getWeakestElement(),
                result.getYangCount(),
                result.getYinCount()));
        result.setYearFortune(interpretYearFortune(result, today));
        result.setTodayFortune(generateTodayFortune(result, today));
    }

