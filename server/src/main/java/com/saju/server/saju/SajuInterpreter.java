    public static String interpretSinsal(List<SinsalInfo> sinsalList) {
        if (sinsalList == null) return "";
        List<SinsalInfo> present = sinsalList.stream().filter(SinsalInfo::isPresent).collect(Collectors.toList());
        if (present.isEmpty()) return "특별한 신살이 발견되지 않았습니다.";
        StringBuilder sb = new StringBuilder();
        for (SinsalInfo s : present) {
            sb.append("▸ ").append(s.getName());
            if (s.getFoundInPillar() != null) sb.append(" (").append(s.getFoundInPillar()).append(")");
            sb.append(": ").append(s.getDescription()).append("\n");
        }
        return sb.toString().trim();
    }

