    /**
     * 대운 상세 해석
     */
    public static String interpretDaeun(List<DaeunInfo> daeunList) {
        if (daeunList == null || daeunList.isEmpty()) return "";
        StringBuilder sb = new StringBuilder();

        DaeunInfo current = null;
        for (DaeunInfo d : daeunList) {
            if (d.isCurrent()) { current = d; break; }
        }

        if (current != null) {
            sb.append("【현재 대운: ").append(current.getFullHanja()).append("(").append(current.getFullName()).append(") ")
              .append(current.getStartAge()).append("~").append(current.getEndAge()).append("세】\n");
            sb.append(interpretSipsungForDaeun(current.getSipsung()));
            sb.append("\n12운성 '").append(current.getTwelveStage()).append("'의 기운이 함께하여 ");
            sb.append(interpretTwelveStageEnergy(current.getTwelveStage()));
            sb.append("\n\n");
        }

        // 다음 대운 미리보기
        boolean foundCurrent = false;
        for (DaeunInfo d : daeunList) {
            if (foundCurrent) {
                sb.append("【다음 대운: ").append(d.getFullHanja()).append("(").append(d.getFullName()).append(") ")
                  .append(d.getStartAge()).append("~").append(d.getEndAge()).append("세】\n");
                sb.append(d.getSipsung()).append(" 운으로 전환됩니다. ");
                sb.append(interpretSipsungForDaeun(d.getSipsung()));
                break;
            }
            if (d.isCurrent()) foundCurrent = true;
        }

        return sb.toString().trim();
    }

