/** 색상 단어를 받아 코디 정보 반환. 부분 매칭 지원 (예: "다크 네이비" → 네이비) */
export function mapLuckyOutfit(colorName) {
  if (!colorName) return null;
  const name = String(colorName).trim();
  if (COLOR_OUTFITS[name]) return { color: name, ...COLOR_OUTFITS[name] };
  // 부분 매칭
  for (const key of Object.keys(COLOR_OUTFITS)) {
    if (name.includes(key) || key.includes(name)) {
      return { color: name, ...COLOR_OUTFITS[key] };
    }
  }
  return { color: name, ...FALLBACK };
}

export default mapLuckyOutfit;
